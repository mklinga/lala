  <p>
  Äetsäläinen Romulan Taiteellinen Teatteri ja laulaja Piritta Ruutikainen saivat vuonna 2010 Pirkanmaan taidetoimikunnan apurahan, jolla Laitoslaulaja-pilottihanke starttasi.
  </p>

  <p>
    Piritta Ruutikainen kertoo:
  </p>
  <blockquote class="historia-quote">
    ”Keksin termin laitoslaulaja ja suunnittelin laitoslaulajan toimenkuvan. Yksi asia oli minulle alusta
    asti selvä: työskentelisin vanhusten kanssa. Mielessäni pyöri koko ajan ajatus, miten voisin laululla
    piristää yksittäisten vanhusten arkea. Halusin kohdata heidät henkilökohtaisesti. Minua kuitenkin
    arvelutti, olisiko se ajan ja rahan tuhlausta, jos kolmessa kuukaudessa tapaisinkin vain muutaman
    vanhuksen enkä edistäisikään mahdollisimman monen hyvinvointia.
    Rohkaisin mieleni ja esitin ohjausryhmälle ajatukseni henkilökohtaisista kohtaamisista. Menisin
    yksinäisen vanhuksen luo joko laulamaan hänen kanssaan, laulamaan hänelle tai kuuntelemaan
    musiikkia ja keskustelemaan kuullusta musiikista. Kerroin, että en näe itseäni pitämässä
    musiikkiesityksiä päiväsalissa tai vetämässä yhteislaulua, vaan haluan tehdä jotain sellaista, mitä ei
    vielä niin paljon tehdä, jos ollenkaan.
    Hankkeen ohjausryhmä innostui ajatuksistani. Viisi kohtaamista -konsepti helpotti aikataulujen
    laatimista ja mahdollisti käynnit useamman vanhuksen luona. Hankkeesta saimme todella
    myönteisiä tuloksia: musiikin huomattiin parantavan laitoksessa asuvien vanhusten elämänlaatua.
    Syksyllä 2012 Pirkanmaan Taidetoimikunta rahoitti Laitoslaulajat vanhuksen hyvinvoinnin tukena -
    hanketta, joka oli Sastamalan Sotesin, kulttuuripalveluiden ja Romulan Taiteellisen Teatterin
    yhteistyöhanke. Tärkeitä henkilöitä hankkeen eteenpäin viemisessä olivat Marita Pitkänen ja
    Terttu Sulanne. Minä ja Maaria Hartman toimimme hankkeessa laitoslaulajina.”
  </blockquote>
